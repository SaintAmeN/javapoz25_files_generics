package com.sda.javapoz25.gnf.generics.before;

public class Para {
    private Object lewa;
    private Object prawa;

    public Para(Object lewa, Object prawa) {
        this.lewa = lewa;
        this.prawa = prawa;
    }

    public Object getLewa() {
        return lewa;
    }

    public void setLewa(Object lewa) {
        this.lewa = lewa;
    }

    public Object getPrawa() {
        return prawa;
    }

    public void setPrawa(Object prawa) {
        this.prawa = prawa;
    }
}
